import { z } from "zod";

export type JsonPrimitive = string | number | boolean | null;
export type JsonValue = JsonPrimitive | JsonValue[] | { [key: string]: JsonValue };
export type JsonObject = Record<string, JsonValue>;

export const jsonValueShape: z.ZodType<JsonValue> = z.lazy(() =>
  z.union([
    z.string(),
    z.number(),
    z.boolean(),
    z.null(),
    z.array(jsonValueShape),
    z.record(jsonValueShape),
  ]),
);

export const jsonObjectShape: z.ZodType<JsonObject> = z.record(jsonValueShape);
